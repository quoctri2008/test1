const student = {
    name: "tri",
    age: 33,
    gender: "male",
};

// Object.keys() -> trả về 1 mảng chứa tất cả các keys của object
console.log(Object.keys(student)); // ["name", "age", "gender"]

// Object.values() -> trả về 1 mảng chứa các giá trị của object
console.log(Object.values(student));