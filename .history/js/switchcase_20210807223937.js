// switch, case
const fruit = "apple";
if (fruit === "apple") {
    console.log("i will eat apple");
}
if (fruit === "orange") {
    console.log("i will eat orange");
}
if (fruit === "lemon") {
    console.log("i will eat lemon");
}

switch (fruit) {
    case "apple":
        console.log("i will eat apple");
        break;
    case "apple":
        console.log("i will eat apple");
        break;
    default:
        break;
}